# Create different types of vectors
char_vec <- c("Red", "Green", "Blue")
num_vec <- c(10, 20, 30, 40)
log_vec <- c(TRUE, FALSE, TRUE)

# Create a list containing all vectors
my_list <- list(
  Character_Data = char_vec,
  Numeric_Data = num_vec,
  Logical_Data = log_vec
)

# Print the list
print(my_list)