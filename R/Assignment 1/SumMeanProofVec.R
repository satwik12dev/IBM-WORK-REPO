# Create a vector
data <- c(2, 4, 6, 8, 10)

# Calculate sum
total_sum <- sum(data)

# Calculate mean
avg <- mean(data)

# Calculate product
prod_value <- prod(data)

# Display results
cat("Vector:", data, "\n")
cat("Sum =", total_sum, "\n")
cat("Mean =", avg, "\n")
cat("Product =", prod_value, "\n")